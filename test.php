以走多久？这样的6年我们还有几个？想着想着我就开始难过，打算敲门，想想还是算了吧&hellip;&hellip;<br/>    <br/>    就这样子，睁着双眼，了无睡意，晚上读圣经说：上主是全善的，容不得一点点罪恶，我觉得自己有好多罪过，可是很感谢现在的我正说着这些与显示格格不入的话还有一个人在电话那头接受着，会明白，真的感谢我的在天之父。你说我们这是怎么啦？在不知不觉中就爱上了难过恋上了忧伤？突然就想起你坐在我身边就这样呆着，你知道吗？我每天晚上都守着你的被子睡，开始想以前，对的错的，慢慢浮出来，你看，我有点tongxinglian的，爱上了你的存在，迷恋上了有你的陪伴，水果篮子的歌，让我想起了一起通宵回来的早晨，回家补觉，听的心咚咚的，无法阻止那些伤害及坦然迎接吧，总有一天有一个地方会让我们好好的歇一歇，不会再痛，你看：我想吸毒一样，缠住了一名生命张扬的女子，睡吧，总会睡着的<br/>    <br/>    09年2月10日00：08<br/>    <br/>    薇『你的短信』<br/>    <br/>    </p>  
